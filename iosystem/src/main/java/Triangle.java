public class Triangle {
    private long p1;
    private long p2;
    private long p3;

    public Triangle(long p1, long p2, long p3) {
        this.p1 = p1;
        this.p2 = p2;
        this.p3 = p3;
    }

    public long getP1() {
        return p1;
    }

    public long getP2() {
        return p2;
    }

    public long getP3() {
        return p3;
    }
}
